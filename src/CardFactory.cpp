namespace dominion {
    static CardFactory *sgInstance = NULL;
    //-----------------------------------------------------------------------------
    // インスタンスの作成
    //-----------------------------------------------------------------------------
    void CardFactory::createInstance(void) {
        if(sgInstance == NULL) {
            sgInstance = new CardFactory();
        }
    }
    //-----------------------------------------------------------------------------
    // インスタンスの削除
    //-----------------------------------------------------------------------------
    void CardFactory::deleteInstance(void) {
        if(sgInstance != NULL) {
            delete sgInstance;
            sgInstance = NULL;
        }
    }
    //-----------------------------------------------------------------------------
    // インスタンスの取得
    //-----------------------------------------------------------------------------
    CardFactory *CardFactory::getInstance(void) {
        return sgInstance;
    }
    //-----------------------------------------------------------------------------
    // コンストラクタ
    //-----------------------------------------------------------------------------
    CardFactory::CardFactory() {}
    //-----------------------------------------------------------------------------
    // デストラクタ
    //-----------------------------------------------------------------------------
    CardFactory::~CardFactory() {}
    //-----------------------------------------------------------------------------
    // カードの作成
    //-----------------------------------------------------------------------------
    Card *CardFactory::createCard(const char *cardMasterName) {
        std::map <std::string, CardMaster *>::iterator it = mMasters.find(cardMasterName);
        if(it == mMasters.end()) {
            return NULL;
        }
        // このカード効果を参照するカードを作成
        Card *card = new Card(it->second);
        return card;
    }
    //-----------------------------------------------------------------------------
    // カード効果の追加
    //-----------------------------------------------------------------------------
    void CardFactory::addCardMaster(CardMaster *cardMaster) {
        if(cardMaster == NULL) return;
        mMasters.insert(std::make_pair(cardMaster->getName(), cardMaster));
    }
    
}
